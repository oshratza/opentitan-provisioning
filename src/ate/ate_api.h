typedef struct {
  // Endpoint address in gRPC name-syntax format, including port number. For
  // example: "localhost:5000", "ipv4:127.0.0.1:5000,127.0.0.2:5000", or
  // "ipv6:[::1]:5000,[::1]:5001".
  // Using a single address will disable load balancing.
  const char* pa_target;

  // gRPC load balancing policy. If not set, it will be selected by the gRPC
  // library. For example: "round_robin" or "pick_first". Leaving this field
  // empty will use the default policy.
  const char* load_balancing_policy;

  // File containing the Client certificate in PEM format. Required when
  // `enable_mtls` set to true.
  const char* pem_cert_chain;

  // File containing the Client secret key in PEM format. Required when
  // `enable_mtls` set to true.
  const char* pem_private_key;

  // File containing the Server root certificates in PEM format. Required when
  // `enable_mtls` set to true.
  const char* pem_root_certs;

  // SKU authentication tokens. These tokens are considered secrets and are
  // used to perform authentication at the client gRPC call level.
  const char* sku_tokens;

  // Set to true to enable mTLS connection. When set to false, the connection
  // is established with insecure credentials.
  bool enable_mtls;
} client_options_t;

/**
 * A SPI console frame sent/received to/from the DUT.
 *
 * For frames sent to the DUT (from the ATE), each must prepended with a 12-byte
 * header that indicates how big the data frame is. This header is ommitted in
 * this struct. The ATE test framework is expected to handle the header and the
 * payload separately.
 *
 * Note: the header does NOT apply to frames received from the DUT.
 */
typedef struct dut_spi_frame {
  /**
   * The data payload to be sent out from the DUT to the ATE.
   *
   * The maximum size of a payload is kDutTxMaxSpiFrameSizeInBytes (which is
   * larger than kDutRxSpiFrameSizeInBytes).
   */
  uint8_t payload[kDutTxMaxSpiFrameSizeInBytes];
  /**
   * The number bytes contained in the payload above.
   *
   * This value is fixed to kDutRxSpiFrameSizeInBytes for SPI frames sent from
   * the ATE to the DUT.
   */
  size_t size;
} dut_spi_frame_t;

/**
 * The perso blob is a structure used to store the personalization data.
 *
 * This structure is used to send and receive the personalization data between
 * the DUT and the ATE.
 *
 * The personalization data may include any of the following:
 * - Device ID. Sent by by the DUT to the ATE.
 * - Signature over TBS certificate data. Sent by the DUT to the ATE.
 * - To-Be-Signed (TBS) certificate data. Sent by the DUT to the ATE.
 * - Endorsed certificates. Sent by the ATE to the DUT.
 */
typedef struct perso_blob {
  /**
   * The number of objects in the blob.
   */
  size_t num_objects;
  /**
   * The offset of the next free object in the blob. Also the size in bytes.
   */
  size_t next_free;
  /**
   * The body of the blob. The size of the body is limited to 5120 bytes.
   */
  uint8_t body[kPersoBlobMaxSize];
} perso_blob_t;

